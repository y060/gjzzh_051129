document.addEventListener('DOMContentLoaded', function() {
    var calendarEl = document.getElementById('calendar');
  
    var calendar = new FullCalendar.Calendar(calendarEl, {

        firstDay: 0,    // 從星期天開始
        selectable: false,    // 活動不能被拖曳

        locale: "zh-tw",

        // 回到2020-05-26的按鈕
        customButtons: {
            FirstMeetingBtn: {
                text: "回到初相見的那天", 
                click: function () {
                    calendar.gotoDate( "2020-05-26" );
                }
            }
        },

        headerToolbar: {
            left: "prev", 
            center: "title",
            right: "FirstMeetingBtn,today next"
        },

        buttonText:{
            prev: "<",
            next: ">",
            today: "今天",
        },

        // 活動
        eventOrder : "displayOrder",
        eventSources: [ 
            { events: Festival, displayOrder: 1, color: "#FFFFE1", textColor: "black" },
            { events: THEIR_EventList, displayOrder: 2, color: "#7c91d0" }, 
            { events: GJ_EventList, displayOrder: 3, color: "#FF2D32" }, 
            { events: ZZH_EventList, displayOrder: 4, color: "#5450FF" }, 
            { events: GZP_EventList, displayOrder: 5, color: "#FF9F54" }
        ],
        
        // 點擊
        eventClick: function(Ev){
            //活動日期
            document.getElementById("event-date").innerHTML = moment(Ev.event.start).format("YYYY-MM-DD");
            if( Ev.event.end != null ){
                document.getElementById("event-date").innerHTML = moment(Ev.event.start).format("YYYY-MM-DD") + " ~ " + moment(Ev.event.end).format("YYYY-MM-DD");
            }
            
            //活動標題
            document.getElementById("event-title").innerHTML = Ev.event.title;
            console.log(Ev.event.title);

            // 活動詳細內容
            if( Ev.event.extendedProps.description != null ){
                document.getElementById("event-info").innerHTML = Ev.event.extendedProps.description;
                console.log(Ev.event.extendedProps.description);
            }
            else{
                document.getElementById("event-info").innerHTML = "";
            }
            // 活動位置
            if( Ev.event.extendedProps.location != null ){
                document.getElementById("event-loca").innerHTML = Ev.event.extendedProps.location;
                console.log(Ev.event.extendedProps.location);
            }
            else{
                document.getElementById("event-loca").innerHTML = "";
            }
            // 活動圖片（設為背景）
            if( Ev.event.extendedProps.bg_img != null ){
                document.getElementById("body").style.backgroundImage = "url(" + Ev.event.extendedProps.bg_img + ")";
                console.log(Ev.event.extendedProps.bg_img);
            }
            else{
                document.getElementById("body").style.backgroundImage = "url(./pic/homepage.jpg)";
            }
        }

    });
  
    calendar.render();

});
